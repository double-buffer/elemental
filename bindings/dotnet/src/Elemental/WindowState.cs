namespace Elemental;

/// <summary>
/// Enumerates the possible states of a window.
/// </summary>
public enum WindowState
{
    /// <summary>
    /// Window is in normal state.
    /// </summary>
    Normal = 0,

    /// <summary>
    /// Window is minimized.
    /// </summary>
    Minimized = 1,

    /// <summary>
    /// Window is maximized.
    /// </summary>
    Maximized = 2,

    /// <summary>
    /// Window is in full screen mode.
    /// </summary>
    FullScreen = 3
}
