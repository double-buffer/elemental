namespace Elemental.Tools;

/// <summary>
/// Stage of a shader.
/// </summary>
public enum ToolsShaderStage
{
    /// <summary>
    /// None shader stage.
    /// </summary>
    None = 0,

    /// <summary>
    /// Amplification shader stage.
    /// </summary>
    AmplificationShader = 1,

    /// <summary>
    /// Mesh shader stage.
    /// </summary>
    MeshShader = 2,

    /// <summary>
    /// Pixel shader stage.
    /// </summary>
    PixelShader = 3
}